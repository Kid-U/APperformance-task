function PowerUps (list: Image[], num: number, postion1: number, postion2: number) {
    if (num == 0) {
        PowerUp = sprites.create(list[num], SpriteKind.NormalMushroom)
        tiles.placeOnTile(PowerUp, tiles.getTileLocation(postion1, postion2))
    }
    if (num == 1) {
        PowerUp = sprites.create(list[num], SpriteKind.GreenMushroom)
        tiles.placeOnTile(PowerUp, tiles.getTileLocation(postion1, postion2))
    }
    if (num == 2) {
        PowerUp = sprites.create(list[num], SpriteKind.iceFlower)
        tiles.placeOnTile(PowerUp, tiles.getTileLocation(postion1, postion2))
    }
    if (num == 3) {
        PowerUp = sprites.create(list[num], SpriteKind.fireFlower)
        tiles.placeOnTile(PowerUp, tiles.getTileLocation(postion1, postion2))
    }
    PowerUp.ay = 50
}
